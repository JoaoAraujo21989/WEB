    <h5 class="card-title">Centro Social e Cultural de S.Pedro de Bairro</h5>
    <p class="card-text">O Centro Social e Cultural de S. Pedro de Bairro é uma Instituição Particular de Solidariedade Social, de utilidade pública sem fins lucrativos. Foi fundada como Associação em 13 de Dezembro de 1983, tendo vindo a aumentar a prestação de serviços gradualmente, procurando corresponder às necessidades sentidas pela comunidade.
    <p class="card-text">O âmbito da acção é regional, privilegiando o concelho de V. N. Famalicão. Tem como objetivo o desenvolvimento, a promoção social e cultural e a criação de respostas conducentes à minimização das carências da comunidade local e concelhia.</p>
    <p class="card-text">No sentido de continuamente operacionalizar este objetivo, a Instituição tem procurado implementar respostas de âmbito diversificado, pelo dispõe atualmente 11 valências nomeadamente:</p>
    <ul>
      <li>Creche</li>
      <li>Jardim de Infância</li>
      <li>Atividades de Tempos Livres</li>
      <li>Centro de Dia, Centro Convivío</li>
      <li>Centro de Atividades Ocupacionais</li>
      <li>Centro de Apoio Familiar e Acompanhamento Parental</li>
      <li>Estrutura Residencial para Idosos</li>
      <li>Serviço de Apoio Domiciliário</li>
      <li>Formação Profissional</li>
      <li>Serviço Atendimento e Acompanhamento Social</li>
    </ul>
    <p class="card-text">Atende diariamente 600 utentes, intervindo no sentido da promoção do bem estar biopsicossocial, procurando satisfazer, potenciar capacidades, criando oportunidades de concretização de interesses e motivações.</p>
    <p class="card-text">Ao longo de 35 anos da sua existência, orientou-se as suas ações por valores nobres como a Humanização, Solidariedade, Doação, Partilha, Amizade, Amizade, Compreensão, Fraternidade e Igualdade e com eles conseguiu construir os alicerces de um espírito de solidariedade que caminha no sentido de corresponder as exigências da comunidade.</p>
